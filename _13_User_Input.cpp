#include <iostream>
using namespace std;

int main (){
    /*
        cout is used to output print values 
        and cin to get user input

        cin is a predefined variable that reads data from the keyboard with the extraction operator >>.

        for example:
    */
        string name; 
        cout << "Type your name : ";        // Type your name and press enter
        cin >> name;                        // Get user input from the keyboard
        cout << "Your name is : " << name;  // Display the input value   
}