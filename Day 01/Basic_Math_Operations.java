public class Basic_Math_Operations {
    public static void main(String[] args)
    {

        int num1 = 10;
        float num2 = 10.5f;        

        System.out.println();
        System.out.println("num1 : " + num1);
        System.out.println("num1 : " + num2);

        System.out.println("Sum of num1 & num2 : " + (num1+num2));
        System.out.println();

        System.out.println("Subtraction of num1 & num2 : " + (num1-num2));
        System.out.println();

        System.out.println("Multiplication of num1 & num2 : " + (num1*num2));
        System.out.println();

        System.out.println("Division of num of num1 & num2 : " + (num1/num2));
    }
}